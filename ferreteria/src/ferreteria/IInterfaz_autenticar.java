    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String us=user.getText();
         String contra=pass.getText();
         if(con.ingresar(us, contra)){
        principal main = new principal();
        main.setExtendedState(main.getExtendedState() | main.MAXIMIZED_BOTH);
        main.setVisible(true); 
        //llenamos el usuario en el otro formulario
        Conexion cc=new Conexion();
        Connection cn= cc.getConnection();
        Statement st=null;
        ResultSet rs;
             try {
                 st=(Statement) cn.createStatement();
                 rs=st.executeQuery("select * from logines where CEDULA = '"+user.getText()+"'");
                 if(rs.next())
        {this.nombre=rs.getString("NOMBRE")+" ";this.cargo=rs.getString("CARGO");
        this.nombre=this.nombre+rs.getString("APELLIDOS");}
             } catch (SQLException ex) {
                 Logger.getLogger(IInterfaz_autenticar.class.getName()).log(Level.SEVERE, null, ex);
             }
        principal.usuario.setText(this.nombre+" ("+this.cargo+") -"+user.getText());
        
        this.dispose();
         }
         else{
             JOptionPane.showMessageDialog(this, "Datos Incorrectos");
         } 
    }//GEN-LAST:event_jButton1ActionPerformed

