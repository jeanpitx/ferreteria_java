    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Conexion cc=new Conexion();
        Connection cn= cc.getConnection();
        Statement st=null;
        ResultSet rs;
        try {
            st=(Statement) cn.createStatement();
            rs=st.executeQuery("SELECT * FROM `credito` WHERE ID_USUARIO = '" + cedula.getText()+ "' AND ID_CREDITO = (SELECT MAX( ID_CREDITO ) FROM CREDITO WHERE ID_USUARIO='"+ cedula.getText() +"' ) ");
            if(rs.next())
            {
                JOptionPane.showMessageDialog(this, "El cliente tiene credito, vaya al botn nuevo pago o cierre");
                M_I.setText(rs.getString("credito.valor_deber"));
                M_I.setEnabled(true);
               
            } else {
    interfazIngresarCliente a= new interfazIngresarCliente();
    principal.panel.add(a);
    a.toFront();
    a.setVisible(true);
    a.setbusqued("credito");
    //empieza codigo de llamadas
    JTextField halo=(JTextField)interfazIngresarCliente.cedula;
    halo.setText(cedula.getText());
                JOptionPane.showMessageDialog(this, "El cliente no tiene credito registrado, por favor seleccionelo y REGISTRELO");
            }
        } catch (SQLException ex) {
            Logger.getLogger(interfazIngresarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void guarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guarActionPerformed
        Conexion cc=new Conexion();
        Connection cn= cc.getConnection();

        String ce= cedula.getText();
        String m= M_I.getText();

        strSQL2="insert into credito(id_usuario, valor_deber,valor_ultimo_pago) values (?,?,?)";
        try {
            PreparedStatement pst = cn.prepareStatement(strSQL2);
            pst.setString(1, ce);
            pst.setString(2, m);
            pst.setString(3, "0");         
           
            int n=pst.executeUpdate();
            if(n>0){
               JOptionPane.showMessageDialog(this, "Datos registrado con exito..!!");
                M_I.setText("");

            }
        } catch (SQLException ex) {
            Logger.getLogger(interfazIngresarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        

    }//GEN-LAST:event_guarActionPerformed

